int		ft_max(int *tab, int length)
{
	int		i;
	int		max;

	if (length < 1)
		return (0);
	i = 0;
	max = -2147483648;
	while (i < length)
	{
		if (tab[i] > max)
			max = tab[i];
		i++;
	}
	return (max);
}
