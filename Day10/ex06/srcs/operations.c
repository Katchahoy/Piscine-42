long	ft_add(long nb1, long nb2)
{
	return (nb1 + nb2);
}

long	ft_sub(long nb1, long nb2)
{
	return (nb1 - nb2);
}

long	ft_mul(long nb1, long nb2)
{
	return (nb1 * nb2);
}

long	ft_div(long nb1, long nb2)
{
	return (nb1 / nb2);
}

long	ft_mod(long nb1, long nb2)
{
	return (nb1 % nb2);
}
